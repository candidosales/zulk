          <p>
            O quarto dia de aventuras da zulk em Paris foi destinado à visita e exploração do Château de Versailles, um dos maiores e mais luxuosos castelos do mundo, construído na cidade de Versalhes pelo Rei Luis XIV, o “Rei Sol”.</p>
          <p>Só a título de curiosidade, aí seguem alguns dados técnicos que encontrei na wikipédia sobre esse castelo: possui 2.153 janelas, 67 escadas, 352 chaminés, 700 quartos, 1.250 lareiras e 700 hectares de parque. É um dos pontos turísticos mais visitados da França, recebe em média oito milhões de turistas por ano.</p>
          <p>E é com base nesse último dado técnico que vamos aqui lhes dar  valiosas dicas para esse passeio.</p>
          <p>Se pretende conhecer tudo com calma, apreciando cada obra de arte ali existente e fazendo lindas e lindas fotos, reserve o dia todo só para esse passeio, foi assim que fizemos. Procure chegar cedo, o castelo está aberto para visitação apartir das 9h da manhã. A taxa para entrar custa 18 euros e menor de idade não paga nada.</p>
          <p>E agora vai a melhor dica de todas: se for verão e a previsão for de sol, passe muito protetor solar e pode até mesmo levar guarda-sol pois provavelmente você irá passar horas na fila só para conseguir entrar no castelo. No dia que fomos, passamos cerca de uma hora na fila, no sol quente, até nos sentimos como se estivéssemos aqui em Teresina, com tanto sol.</p>
          <p>Depois que terminar a visita pelo castelo você deve visitar o imenso e belíssimo jardim onde poderá fazer fotos maravilhosas. E após toda a visitação você pode almoçar em Versalhes mesmo, bem próximo ao castelo tem uma galeria com vários restaurantes e fast-foods. Depois do almoço você pode caminhar um pouco pela cidade e conhecer a prefeitura que fica bem perto e é muito linda para mais fotos e também outras coisas da cidade como lojinhas de souvenirs.
          </p>
